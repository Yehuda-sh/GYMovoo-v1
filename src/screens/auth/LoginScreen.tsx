import React, { useState, useEffect, useRef } from "react";
import {
  View,
  Text,
  TextInput,
  StyleSheet,
  TouchableOpacity,
  KeyboardAvoidingView,
  Platform,
  ActivityIndicator,
  Animated,
  Switch,
  Alert,
} from "react-native";
import { useNavigation, useRoute } from "@react-navigation/native";
import { Ionicons, MaterialIcons } from "@expo/vector-icons";
import { LinearGradient } from "expo-linear-gradient";
import AsyncStorage from "@react-native-async-storage/async-storage";
import { theme } from "../../styles/theme";
import BackButton from "../../components/common/BackButton";
import { fakeGoogleSignIn } from "../../services/authService";
import { useUserStore } from "../../stores/userStore";

// ◊§◊ï◊†◊ß◊¶◊ô◊ï◊™ ◊¢◊ñ◊® ◊ú◊ê◊†◊ô◊û◊¶◊ô◊ï◊™ // Animation helper functions
/**
 * ◊ô◊ï◊¶◊® ◊ê◊†◊ô◊û◊¶◊ô◊ô◊™ ◊®◊¢◊ô◊ì◊î ◊ú◊ê◊ú◊û◊†◊ò
 * Creates shake animation for element
 */
const createShakeAnimation = (value: Animated.Value) => {
  return Animated.sequence([
    Animated.timing(value, {
      toValue: 10,
      duration: 50,
      useNativeDriver: true,
    }),
    Animated.timing(value, {
      toValue: -10,
      duration: 50,
      useNativeDriver: true,
    }),
    Animated.timing(value, {
      toValue: 10,
      duration: 50,
      useNativeDriver: true,
    }),
    Animated.timing(value, { toValue: 0, duration: 50, useNativeDriver: true }),
  ]);
};

export default function LoginScreen() {
  const navigation = useNavigation<any>();
  const route = useRoute<any>();

  console.log("üîê LoginScreen - Component mounted");

  // States
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [showPassword, setShowPassword] = useState(false);
  const [rememberMe, setRememberMe] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const [fieldErrors, setFieldErrors] = useState<{
    email?: string;
    password?: string;
  }>({});

  // ◊ê◊†◊ô◊û◊¶◊ô◊ï◊™ // Animations
  const fadeAnim = useRef(new Animated.Value(0)).current;
  const shakeAnim = useRef(new Animated.Value(0)).current;
  const scaleAnim = useRef(new Animated.Value(1)).current;

  useEffect(() => {
    // ◊ò◊¢◊ô◊†◊™ ◊§◊®◊ò◊ô◊ù ◊©◊û◊ï◊®◊ô◊ù // Load saved credentials
    console.log("üîê LoginScreen - useEffect triggered");
    loadSavedCredentials();

    // ◊ê◊†◊ô◊û◊¶◊ô◊ô◊™ ◊õ◊†◊ô◊°◊î // Entry animation
    Animated.timing(fadeAnim, {
      toValue: 1,
      duration: 600,
      useNativeDriver: true,
    }).start(() => {
      console.log("üîê LoginScreen - Entry animation completed");
    });

    // ◊î◊§◊¢◊ú◊™ Google ◊ê◊ï◊ò◊ï◊û◊ò◊ô ◊ê◊ù ◊î◊í◊ô◊¢ ◊¢◊ù google: true
    if (route?.params?.google) {
      console.log(
        "üîê LoginScreen - Auto Google login triggered from route params"
      );
      handleGoogleAuth();
    }
  }, [route?.params?.google]);

  /**
   * ◊ò◊ï◊¢◊ü ◊§◊®◊ò◊ô ◊î◊™◊ó◊ë◊®◊ï◊™ ◊©◊û◊ï◊®◊ô◊ù ◊û-AsyncStorage
   * Loads saved credentials from AsyncStorage
   */
  const loadSavedCredentials = async () => {
    try {
      console.log("üîê LoginScreen - Loading saved credentials...");
      const savedEmail = await AsyncStorage.getItem("savedEmail");
      if (savedEmail) {
        console.log("üîê LoginScreen - Found saved email:", savedEmail);
        setEmail(savedEmail);
        setRememberMe(true);
      } else {
        console.log("üîê LoginScreen - No saved credentials found");
      }
    } catch (error) {
      console.error("üîê LoginScreen - Failed to load saved email:", error);
    }
  };

  /**
   * ◊ë◊ï◊ì◊ß ◊™◊ß◊ô◊†◊ï◊™ ◊õ◊™◊ï◊ë◊™ ◊ê◊ô◊û◊ô◊ô◊ú
   * Validates email format
   */
  const validateEmail = (email: string): boolean => {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
  };

  /**
   * ◊ë◊ï◊ì◊ß ◊™◊ß◊ô◊†◊ï◊™ ◊°◊ô◊°◊û◊î
   * Validates password requirements
   */
  const validatePassword = (password: string): boolean => {
    return password.length >= 6;
  };

  /**
   * ◊ë◊ï◊ì◊ß ◊™◊ß◊ô◊†◊ï◊™ ◊õ◊ú ◊î◊ò◊ï◊§◊°
   * Validates entire form
   */
  const validateForm = (): boolean => {
    console.log("üîê LoginScreen - Validating form...");
    const errors: typeof fieldErrors = {};

    if (!email) {
      errors.email = "◊ê◊†◊ê ◊î◊ñ◊ü ◊õ◊™◊ï◊ë◊™ ◊ê◊ô◊û◊ô◊ô◊ú";
    } else if (!validateEmail(email)) {
      errors.email = "◊õ◊™◊ï◊ë◊™ ◊ê◊ô◊û◊ô◊ô◊ú ◊ú◊ê ◊™◊ß◊ô◊†◊î";
    }

    if (!password) {
      errors.password = "◊ê◊†◊ê ◊î◊ñ◊ü ◊°◊ô◊°◊û◊î";
    } else if (!validatePassword(password)) {
      errors.password = "◊î◊°◊ô◊°◊û◊î ◊ó◊ô◊ô◊ë◊™ ◊ú◊î◊õ◊ô◊ú ◊ú◊§◊ó◊ï◊™ 6 ◊™◊ï◊ï◊ô◊ù";
    }

    setFieldErrors(errors);

    if (Object.keys(errors).length > 0) {
      console.log("üîê LoginScreen - Validation failed:", errors);
      createShakeAnimation(shakeAnim).start();
      return false;
    }

    console.log("üîê LoginScreen - Validation passed ‚úÖ");
    return true;
  };

  /**
   * ◊û◊ò◊§◊ú ◊ë◊™◊î◊ú◊ô◊ö ◊î◊î◊™◊ó◊ë◊®◊ï◊™
   * Handles login process
   */
  const handleLogin = async () => {
    console.log("üîê LoginScreen - Login attempt started");
    console.log("üîê LoginScreen - Email:", email);
    console.log("üîê LoginScreen - Password length:", password.length);
    console.log("üîê LoginScreen - Remember me:", rememberMe);

    if (!validateForm()) {
      return;
    }

    setLoading(true);
    setError(null);
    setFieldErrors({});

    // ◊ê◊†◊ô◊û◊¶◊ô◊ô◊™ ◊ú◊ó◊ô◊¶◊î // Press animation
    Animated.sequence([
      Animated.timing(scaleAnim, {
        toValue: 0.95,
        duration: 100,
        useNativeDriver: true,
      }),
      Animated.timing(scaleAnim, {
        toValue: 1,
        duration: 100,
        useNativeDriver: true,
      }),
    ]).start();

    try {
      // ◊©◊û◊ô◊®◊™ ◊ê◊ô◊û◊ô◊ô◊ú ◊ê◊ù ◊†◊ë◊ó◊® "◊ñ◊õ◊ï◊® ◊ê◊ï◊™◊ô" // Save email if remember me
      if (rememberMe) {
        console.log("üîê LoginScreen - Saving email to AsyncStorage");
        await AsyncStorage.setItem("savedEmail", email);
      } else {
        console.log("üîê LoginScreen - Removing saved email from AsyncStorage");
        await AsyncStorage.removeItem("savedEmail");
      }

      // ◊°◊ô◊û◊ï◊ú◊¶◊ô◊ô◊™ ◊î◊™◊ó◊ë◊®◊ï◊™ // Login simulation
      setTimeout(async () => {
        setLoading(false);
        if (email === "test@example.com" && password === "123456") {
          console.log("üîê LoginScreen - Login successful! ‚úÖ");
          const user = {
            email,
            name: "◊û◊©◊™◊û◊© ◊ú◊ì◊ï◊í◊û◊î",
            id: "user123",
            avatar: undefined,
          };

          // ◊©◊û◊ô◊®◊î ◊ë-Zustand // Save to Zustand
          console.log("üîê LoginScreen - Saving user to Zustand store");
          useUserStore.getState().setUser(user);

          // ◊ë◊ì◊ô◊ß◊î ◊ê◊ù ◊ô◊© ◊©◊ê◊ú◊ï◊ü // Check if questionnaire exists
          const hasQuestionnaire = useUserStore.getState().user?.questionnaire;
          console.log(
            "üîê LoginScreen - Has questionnaire?",
            !!hasQuestionnaire
          );

          if (!hasQuestionnaire) {
            console.log("üîê LoginScreen - Navigating to Questionnaire");
            navigation.reset({ index: 0, routes: [{ name: "Questionnaire" }] });
          } else {
            console.log("üîê LoginScreen - Navigating to Main");
            navigation.reset({ index: 0, routes: [{ name: "Main" }] });
          }
        } else {
          console.log("üîê LoginScreen - Login failed - invalid credentials ‚ùå");
          setError("◊õ◊™◊ï◊ë◊™ ◊î◊ê◊ô◊û◊ô◊ô◊ú ◊ê◊ï ◊î◊°◊ô◊°◊û◊î ◊©◊í◊ï◊ô◊ô◊ù");
          createShakeAnimation(shakeAnim).start();
        }
      }, 1200);
    } catch (e) {
      console.error("üîê LoginScreen - Login error:", e);
      setLoading(false);
      setError("◊ê◊ô◊®◊¢◊î ◊©◊í◊ô◊ê◊î ◊ë◊î◊™◊ó◊ë◊®◊ï◊™");
    }
  };

  /**
   * ◊û◊ò◊§◊ú ◊ë◊î◊™◊ó◊ë◊®◊ï◊™ ◊¢◊ù Google
   * Handles Google authentication
   */
  const handleGoogleAuth = async () => {
    console.log("üîê LoginScreen - Google auth started");
    setLoading(true);
    setError(null);

    try {
      console.log("üîê LoginScreen - Calling fakeGoogleSignIn...");
      const googleUser = await fakeGoogleSignIn();
      console.log("üîê LoginScreen - Google user received:", googleUser);

      useUserStore.getState().setUser(googleUser);

      // ◊ë◊ì◊ô◊ß◊î ◊ê◊ù ◊ô◊© ◊í◊ô◊ú ◊™◊ß◊ô◊ü ◊ï◊©◊ê◊ú◊ï◊ü // Check age and questionnaire
      console.log(
        "üîê LoginScreen - Checking questionnaire:",
        googleUser.questionnaire
      );
      if (
        !googleUser.questionnaire ||
        !googleUser.questionnaire[0] ||
        googleUser.questionnaire[0] === "◊û◊™◊ó◊™ ◊ú-16"
      ) {
        console.log(
          "üîê LoginScreen - Google user needs questionnaire, navigating..."
        );
        navigation.reset({ index: 0, routes: [{ name: "Questionnaire" }] });
      } else {
        console.log(
          "üîê LoginScreen - Google user has questionnaire, navigating to Main"
        );
        navigation.reset({ index: 0, routes: [{ name: "Main" }] });
      }
    } catch (e) {
      console.error("üîê LoginScreen - Google auth failed:", e);
      setError("◊î◊î◊™◊ó◊ë◊®◊ï◊™ ◊¢◊ù Google ◊†◊õ◊©◊ú◊î");
    } finally {
      setLoading(false);
    }
  };

  /**
   * ◊û◊ò◊§◊ú ◊ë◊©◊ó◊ñ◊ï◊® ◊°◊ô◊°◊û◊î
   * Handles password recovery
   */
  const handleForgotPassword = () => {
    console.log("üîê LoginScreen - Forgot password clicked");
    Alert.alert(
      "◊©◊ó◊ñ◊ï◊® ◊°◊ô◊°◊û◊î",
      "◊†◊©◊ú◊ó ◊ú◊ö ◊ß◊ô◊©◊ï◊® ◊ú◊ê◊ô◊§◊ï◊° ◊î◊°◊ô◊°◊û◊î ◊ú◊õ◊™◊ï◊ë◊™ ◊î◊ê◊ô◊û◊ô◊ô◊ú ◊©◊ú◊ö",
      [
        {
          text: "◊ë◊ô◊ò◊ï◊ú",
          style: "cancel",
          onPress: () =>
            console.log("üîê LoginScreen - Password reset cancelled"),
        },
        {
          text: "◊©◊ú◊ó",
          onPress: () => {
            console.log(
              "üîê LoginScreen - Password reset requested for:",
              email
            );
            if (!email) {
              setFieldErrors({ email: "◊ê◊†◊ê ◊î◊ñ◊ü ◊õ◊™◊ï◊ë◊™ ◊ê◊ô◊û◊ô◊ô◊ú ◊ú◊©◊ó◊ñ◊ï◊®" });
              return;
            }
            if (!validateEmail(email)) {
              setFieldErrors({ email: "◊õ◊™◊ï◊ë◊™ ◊ê◊ô◊û◊ô◊ô◊ú ◊ú◊ê ◊™◊ß◊ô◊†◊î" });
              return;
            }
            console.log("üîê LoginScreen - Password reset email sent! ‚úÖ");
            Alert.alert("◊†◊©◊ú◊ó!", "◊ß◊ô◊©◊ï◊® ◊ú◊ê◊ô◊§◊ï◊° ◊°◊ô◊°◊û◊î ◊†◊©◊ú◊ó ◊ú◊ê◊ô◊û◊ô◊ô◊ú ◊©◊ú◊ö");
          },
        },
      ]
    );
  };

  return (
    <KeyboardAvoidingView
      style={styles.container}
      behavior={Platform.select({ ios: "padding", android: undefined })}
    >
      <BackButton />

      <Animated.View
        style={[
          styles.formBox,
          {
            opacity: fadeAnim,
            transform: [{ translateX: shakeAnim }],
          },
        ]}
      >
        {/* ◊õ◊ï◊™◊®◊™ ◊¢◊ù ◊í◊®◊ì◊ô◊ê◊†◊ò // Title with gradient */}
        <LinearGradient
          colors={[
            theme.colors.primaryGradientStart,
            theme.colors.primaryGradientEnd,
          ]}
          style={styles.titleGradient}
        >
          <Text style={styles.title}>◊ë◊®◊ï◊ö ◊î◊ë◊ê ◊ó◊ñ◊®◊î!</Text>
        </LinearGradient>

        <Text style={styles.subtitle}>◊î◊™◊ó◊ë◊® ◊õ◊ì◊ô ◊ú◊î◊û◊©◊ô◊ö ◊ë◊ê◊ô◊û◊ï◊†◊ô◊ù ◊©◊ú◊ö</Text>

        {/* ◊©◊ì◊î ◊ê◊ô◊û◊ô◊ô◊ú // Email field */}
        <View style={styles.inputContainer}>
          <View
            style={[
              styles.inputWrapper,
              fieldErrors.email && styles.inputError,
            ]}
          >
            <MaterialIcons
              name="email"
              size={22}
              color={
                fieldErrors.email ? theme.colors.error : theme.colors.accent
              }
              style={{ marginLeft: 6 }}
            />
            <TextInput
              style={styles.input}
              placeholder="◊õ◊™◊ï◊ë◊™ ◊ê◊ô◊û◊ô◊ô◊ú"
              placeholderTextColor={theme.colors.textSecondary}
              keyboardType="email-address"
              autoCapitalize="none"
              autoCorrect={false}
              value={email}
              onChangeText={(text) => {
                setEmail(text);
                setFieldErrors((prev) => ({ ...prev, email: undefined }));
              }}
              textAlign="right"
              editable={!loading}
            />
          </View>
          {fieldErrors.email && (
            <Text style={styles.fieldError}>{fieldErrors.email}</Text>
          )}
        </View>

        {/* ◊©◊ì◊î ◊°◊ô◊°◊û◊î // Password field */}
        <View style={styles.inputContainer}>
          <View
            style={[
              styles.inputWrapper,
              fieldErrors.password && styles.inputError,
            ]}
          >
            <TouchableOpacity
              onPress={() => setShowPassword(!showPassword)}
              disabled={loading}
            >
              <Ionicons
                name={showPassword ? "eye-off" : "eye"}
                size={22}
                color={
                  fieldErrors.password
                    ? theme.colors.error
                    : theme.colors.accent
                }
                style={{ marginLeft: 6 }}
              />
            </TouchableOpacity>
            <TextInput
              style={styles.input}
              placeholder="◊°◊ô◊°◊û◊î"
              placeholderTextColor={theme.colors.textSecondary}
              secureTextEntry={!showPassword}
              autoCapitalize="none"
              autoCorrect={false}
              value={password}
              onChangeText={(text) => {
                setPassword(text);
                setFieldErrors((prev) => ({ ...prev, password: undefined }));
              }}
              textAlign="right"
              editable={!loading}
            />
          </View>
          {fieldErrors.password && (
            <Text style={styles.fieldError}>{fieldErrors.password}</Text>
          )}
        </View>

        {/* ◊ñ◊õ◊ï◊® ◊ê◊ï◊™◊ô ◊ï◊©◊õ◊ó◊™◊ô ◊°◊ô◊°◊û◊î // Remember me & Forgot password */}
        <View style={styles.optionsRow}>
          <View style={styles.rememberMe}>
            <Text style={styles.rememberMeText}>◊ñ◊õ◊ï◊® ◊ê◊ï◊™◊ô</Text>
            <Switch
              value={rememberMe}
              onValueChange={setRememberMe}
              trackColor={{
                false: theme.colors.divider,
                true: theme.colors.primaryGradientStart,
              }}
              thumbColor={rememberMe ? theme.colors.primary : "#f4f3f4"}
              disabled={loading}
            />
          </View>
          <TouchableOpacity onPress={handleForgotPassword} disabled={loading}>
            <Text style={styles.forgotPassword}>◊©◊õ◊ó◊™◊ô ◊°◊ô◊°◊û◊î</Text>
          </TouchableOpacity>
        </View>

        {/* ◊î◊ï◊ì◊¢◊™ ◊©◊í◊ô◊ê◊î ◊õ◊ú◊ú◊ô◊™ // General error message */}
        {error && (
          <Animated.View style={{ transform: [{ scale: scaleAnim }] }}>
            <Text style={styles.errorText}>{error}</Text>
          </Animated.View>
        )}

        {/* ◊õ◊§◊™◊ï◊® ◊î◊™◊ó◊ë◊®◊ï◊™ // Login button */}
        <TouchableOpacity
          style={[styles.loginButton, loading && styles.loginButtonDisabled]}
          onPress={handleLogin}
          disabled={loading}
          activeOpacity={0.8}
        >
          <LinearGradient
            colors={[
              theme.colors.primaryGradientStart,
              theme.colors.primaryGradientEnd,
            ]}
            style={styles.gradientButton}
          >
            {loading ? (
              <ActivityIndicator color="#fff" />
            ) : (
              <Text style={styles.loginButtonText}>◊î◊™◊ó◊ë◊®</Text>
            )}
          </LinearGradient>
        </TouchableOpacity>

        {/* ◊ê◊ï // OR */}
        <View style={styles.dividerContainer}>
          <View style={styles.divider} />
          <Text style={styles.dividerText}>◊ê◊ï</Text>
          <View style={styles.divider} />
        </View>

        {/* ◊õ◊§◊™◊ï◊® Google // Google button */}
        <TouchableOpacity
          style={[styles.googleButton, loading && styles.googleButtonDisabled]}
          onPress={handleGoogleAuth}
          disabled={loading}
          activeOpacity={0.8}
        >
          <Ionicons name="logo-google" size={22} color="#fff" />
          <Text style={styles.googleButtonText}>◊î◊™◊ó◊ë◊® ◊¢◊ù Google</Text>
        </TouchableOpacity>

        {/* ◊ß◊ô◊©◊ï◊® ◊ú◊î◊®◊©◊û◊î // Registration link */}
        <View style={styles.linkRow}>
          <Text style={styles.linkText}>◊ê◊ô◊ü ◊ú◊ö ◊ó◊©◊ë◊ï◊ü ◊¢◊ì◊ô◊ô◊ü?</Text>
          <TouchableOpacity
            onPress={() => navigation.navigate("Register")}
            disabled={loading}
          >
            <Text style={styles.registerLink}>◊î◊®◊©◊ù ◊¢◊õ◊©◊ô◊ï</Text>
          </TouchableOpacity>
        </View>
      </Animated.View>
    </KeyboardAvoidingView>
  );
}

